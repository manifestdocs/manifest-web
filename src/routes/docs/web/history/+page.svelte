<h1>History View</h1>

<p>Timeline of implementation work.</p>

<p>The History view shows a timeline of all implementation work across the project.</p>

<p>
    History entries are created automatically when agents complete work on a feature.
    You don't need to write them manually—agents summarize their work and link commits as they go.
</p>

<h2>Entry details</h2>

<p>Each entry shows:</p>

<ul>
    <li><strong>Feature:</strong> What was worked on</li>
    <li><strong>Summary:</strong> What was done</li>
    <li><strong>Commits:</strong> Linked git commits (if git remote configured)</li>
</ul>

<h2>Grouping</h2>

<p>Entries are grouped by time:</p>

<ul>
    <li>Today (just now, 15 mins ago, 1 hour ago, etc.)</li>
    <li>Yesterday</li>
    <li>Last week</li>
    <li>Older dates</li>
</ul>
